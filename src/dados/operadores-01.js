// Operadores aritméticos

// Usando valores literais
console.log(10 + 5); // 15
console.log(10 - 5); // 5
console.log(10 * 5); // 50
console.log(10 / 5); // 2
console.log(10 % 5); // 0

// Usando variáveis
let x = 10;
let y = 5;

console.log(x + y); // 10 + 5
console.log(x - y); // 10 - 5
console.log(x * y); // 10 * 5
console.log(x / y); // 10 / 5
console.log(x % y); // 10 % 5


// Incremento e decremento
let z = 10;

console.log(z++); // 10
console.log(z);   // 11
console.log(++z); // 12
console.log(z);   // 12

console.log(z--); // 12
console.log(--z); // 10
console.log(z);   // 10

let a = 10;
a *= 5; // a = a * 5;
console.log(a);